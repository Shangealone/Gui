import tkinter
from tkinter import *
win = Tk()
def add(): lblResult.config(text="Results: " + str(float(txt1.get()) + float(txt2.get())))
def subtract(): lblResult.config(text="Results: " + str(float(txt1.get()) - float(txt2.get())))
